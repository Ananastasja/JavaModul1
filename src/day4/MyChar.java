package day4;

public class MyChar {
    public static void main(String[] args) {
        char dollar = '$';
        System.out.println(dollar);
        String myDollar = "$$";
        System.out.println(myDollar);
        char zahl = 100;
        char octal = '\u00A9';
        char tabulator = '\t';
        System.out.println(zahl);
        System.out.println(octal);
        System.out.println(tabulator);

    }
}
